    private void addCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerBtnActionPerformed
        String id = idText.getText();
        String name = nameText.getText();
        String address = addressText.getText();
        String dob = yearCombo.getSelectedItem().toString() + "-" + monthCombo.getSelectedItem().toString() + "-" + dayCombo.getSelectedItem().toString();
        String contact = contactText.getText();
        System.out.println(dob);
        Customer customer = new Customer(id, name, address, dob, contact);

        try {
            int addCustomer = CustomerController.addCustomer(customer);
            if (addCustomer > 0) {
                JOptionPane.showMessageDialog(null, "Customer Added !!!!");
            } else {
                JOptionPane.showMessageDialog(null, "Error !!!!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }

        idText.setText("");
        nameText.setText("");
        addressText.setText("");
        contactText.setText("");

        try {
            idText.setText(generateID());
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addCustomerBtnActionPerformed

    private void contactTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_contactTextKeyReleased
        String txt = contactText.getText();
        int caretPosition = contactText.getCaretPosition();
        if (!txt.matches("^[0-9]{0,10}$")) {
            contactText.setText(txt.substring(0, caretPosition - 1) + txt.substring(caretPosition));
            contactText.setCaretPosition(caretPosition - 1);
        }

        if (!nameText.getText().equals("") && !addressText.getText().equals("") && !contactText.getText().equals("")) {
            addCustomerBtn.setEnabled(true);
        }
    }//GEN-LAST:event_contactTextKeyReleased

