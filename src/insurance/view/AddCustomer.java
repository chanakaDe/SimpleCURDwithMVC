    private String generateID() throws ClassNotFoundException, SQLException {
        String custID = CustomerController.getLastId();
        if (custID == null) {
            return "C0001";
        } else {
            int num = Integer.parseInt(custID.substring(1));
            num++;
            if (num < 10) {
                return "C000" + num;
            } else if (num < 100) {
                return "C00" + num;
            } else if (num < 1000) {
                return "C0" + num;
            } else {
                return null;
            }
        }
    }

